public class KetQuaTuVan {
    private String id;
    private  String tenTruong;
    private   String muchp ;
    private   String muchb;
    private   String tenNganh;
    private   String khoi;
    private float diemChuan;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTenTruong() {
        return tenTruong;
    }

    public void setTenTruong(String tenTruong) {
        this.tenTruong = tenTruong;
    }

    public String getMuchp() {
        return muchp;
    }

    public void setMuchp(String muchp) {
        this.muchp = muchp;
    }

    public String getMuchb() {
        return muchb;
    }

    public void setMuchb(String muchb) {
        this.muchb = muchb;
    }

    public String getKhoi() {
        return khoi;
    }

    public void setKhoi(String khoi) {
        this.khoi = khoi;
    }

    public String getTenNganh() {
        return tenNganh;
    }

    public void setTenNganh(String tenNganh) {
        this.tenNganh = tenNganh;
    }

    public float getDiemChuan() {
        return diemChuan;
    }

    public void setDiemChuan(float diemChuan) {
        this.diemChuan = diemChuan;
    }
    
    
}
